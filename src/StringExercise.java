public class StringExercise {
    public static void main(String[] args) {
    // 1. String basics
    String pinkFloyd = "We don't need no education\nWe don't need no thought control";
    System.out.println(pinkFloyd);

    String checkOut = "Check \"this\" out!, \"s inside of \"s!";
    System.out.println(checkOut);

    String windows = "In windows, the main drive is usually C:\\";
    System.out.println(windows);

    String backslashes = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
    System.out.println(backslashes);
    }
}
